  String get currentLanguageName => _languages[_currentLanguageCode]?['name'] ?? 'English';

  // Load saved language preference
  Future<void> loadLanguagePreference() async {
    try {
      final prefs = await SharedPreferences.getInstance();
      final savedLanguage = prefs.getString('app_language');
      if (savedLanguage != null && _languages.containsKey(savedLanguage)) {
        _currentLanguageCode = savedLanguage;
        notifyListeners();
      }
    } catch (e) {
      debugPrint('Error loading language preference: $e');
    }
  }

  // Change language and persist
  Future<void> changeLanguage(String languageCode) async {
    if (_languages.containsKey(languageCode) && _currentLanguageCode != languageCode) {
      _currentLanguageCode = languageCode;

      // Notify listeneimmediately for instant UI update
      notifyListeners();

      // Save to SharedPreferences asynchronously without awaiting
      SharedPreferences.getInstance().then((prefs) {
        prefs.setString('app_language', languageCode);
      }).catchError((e) {
        debugPrint('Error saving language preference: $e');
      });
    }
  }

